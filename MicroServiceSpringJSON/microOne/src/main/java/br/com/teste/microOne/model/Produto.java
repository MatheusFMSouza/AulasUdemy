package br.com.teste.microOne.model;

public class Produto {

    private Integer id;
    private String name;
    private String price;

    public Produto(){}

    public Produto(String name, String price, Integer id) {
        this.id = id;
        this.name = name;
        this.price = price;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }
}
